for number in range(10): # number - 0 to 9, times 10
    if number % 2 == 0:
        continue
    else:
        print(number)

# | i | Condition | O/P
# | 0 | 0%2 == 0 -> True | Continue - skip no o/p
# | 1 | 1%2 == 0 -> False | 1


